    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        
        try{
           int idUsuario = Integer.parseInt(txtIDEmpleado.getText().trim());
           String usuario = txtUsuario.getText().trim();
           String nombre = txtNombre.getText().trim();
           String turno = cmbTurno.getSelectedItem().toString();
           
           
           
           Empleado empleado = new Empleado(usuario,nombre,turno);
           EmpleadoBD dao = new EmpleadoBD();
           
           try(Connection conn = objetoConexionBD.conexionDataBase()){
               boolean actualizado = dao.actualizarEmpleado(conn, idUsuario, nombre, usuario, turno);
               if(actualizado){
                   JOptionPane.showMessageDialog(null, "Cliente actualizado exitosamente");
                   llenarDataTable();
               }
               else{
                   JOptionPane.showMessageDialog(null, "No se encontro un Cliente con esa ID");
               }
           }catch(SQLException ex){
               JOptionPane.showMessageDialog(null, "Error al actualizar usuario: "+ex.getMessage());
           }
       }catch(NumberFormatException ex){
           JOptionPane.showMessageDialog(null, "ID invalido, verifique que sea un numero");
       }catch(ClassNotFoundException ex){
           JOptionPane.showMessageDialog(null, "Error al cargar el controlador de base de datos: "+ex.getMessage());
       }
    }//GEN-LAST:event_btnActualizarActionPerformed

