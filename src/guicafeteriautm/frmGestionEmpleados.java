        public final void llenarDataTable() throws ClassNotFoundException{
        ArrayList<Empleado> arregloEmpleados = new ArrayList<>();
        String sql = "SELECT idempleado,usuario.usuario,nombre,turno FROM empleado inner join usuario on empleado.idusuario=usuario.idusuario";
        boolean buscar = false;
        
        try{
            conn=objetoConexionBD.conexionDataBase();
            
            if(!busqueda.equals("")){
                sql += " WHERE nombre=?";
                buscar=true;
            }
            
            
            
            System.out.println(sql);
            
            //Preparar parametros
            stmt=conn.prepareStatement(sql);
            if(buscar){
                stmt.setString(1, busqueda);
            }
            
            rs=stmt.executeQuery();
            
            //Verificar si hay resultados
            boolean tieneDatos = false;
            while (rs.next()){
                tieneDatos=true;
                Empleado empleado = new Empleado(
                    rs.getInt("idempleado"),
                    rs.getString("nombre"),
                    rs.getString("usuario.usuario"),
                    rs.getString("turno")
                );
                arregloEmpleados.add(empleado);
            }
            
            if(!tieneDatos){
                JOptionPane.showMessageDialog(null,"Datos no localizados");
            }
            
            //Actualizar Tabla
            DefaultTableModel modelo = (DefaultTableModel) tblEmpleados.getModel();
            modelo.setRowCount(0);//Limpia la tabla
            for (Empleado item : arregloEmpleados){
                Object[] fila = {
                    item.getIdEmpleado(),
                    item.getUsuario(),
                    item.getNombre(),
                    item.getTurno()
                };
                modelo.addRow(fila);
            }
            
            tblEmpleados.setModel(modelo);
            
            //Seleccionar primera fila si hay daatos
            if(tblEmpleados.getRowCount()>0){
                tblEmpleados.setRowSelectionInterval(0,0);
                llenarTextBox();
            }   
        }catch(SQLException ex){
            Logger.getLogger(frmGestionUsuarios.class.getName()).log(Level.SEVERE,null,ex);
        }
    }
    
